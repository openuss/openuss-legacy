package org.openuss.services;

public class LectureLogicException extends Exception {

	private static final long serialVersionUID = 2266503167535413378L;

	public LectureLogicException() {
		super();
	}

	public LectureLogicException(String message, Throwable cause) {
		super(message, cause);
	}

	public LectureLogicException(String message) {
		super(message);
	}

	public LectureLogicException(Throwable cause) {
		super(cause);
	}

}
